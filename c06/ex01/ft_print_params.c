#include <unistd.h>

int	main(int argc, char *argv[])
{
	int	n;
	int	m;

	n = 0;
	m = -1;
	while (++n <= argc)
	{
		while (argv[n][++m] != '\0')
			write(1, argv[n][m], 1);
		write(1, "\n", 1);
		m = -1;
	}
	return (0);
}
